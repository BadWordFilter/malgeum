async function detectLocationAndSetLanguage() {
  const savedLang = localStorage.getItem('malgeum_lang');
  if (savedLang) {
    setLanguage(savedLang);
    return;
  }

  try {
    const response = await fetch('https://ipapi.co/json/');
    const data = await response.json();
    const countryCode = data.country_code;
    
    let lang = 'en';
    if (countryCode === 'KR') lang = 'ko';
    else if (countryCode === 'CN' || countryCode === 'TW' || countryCode === 'HK') lang = 'zh';

    setLanguage(lang);
  } catch (error) {
    console.error('Failed to detect location:', error);
    const browserLang = navigator.language.slice(0, 2);
    let lang = 'en';
    if (browserLang === 'ko') lang = 'ko';
    else if (browserLang === 'zh') lang = 'zh';
    
    setLanguage(lang);
  }
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  
  localStorage.setItem('malgeum_lang', lang);
  document.documentElement.lang = lang;
  
  // Update custom dropdown UI
  document.querySelectorAll('.custom-dropdown').forEach(dropdown => {
    const currentLangSpan = dropdown.querySelector('.current-lang');
    const options = dropdown.querySelectorAll('.dropdown-menu li');
    
    options.forEach(opt => {
      if (opt.dataset.value === lang) {
        opt.classList.add('selected');
        currentLangSpan.textContent = opt.textContent;
      } else {
        opt.classList.remove('selected');
      }
    });
  });
  
  // Static Map Replace
  for (const [selector, key] of Object.entries(domMapping)) {
    const els = document.querySelectorAll(selector);
    els.forEach(el => {
      if (!el) return;
      
      const arrowSpan = el.querySelector('.btn-arrow');
      if (arrowSpan) {
        const arrowHtml = arrowSpan.outerHTML;
        el.innerHTML = translations[lang][key] + ' ' + arrowHtml;
      } else {
        if (translations[lang][key].includes('<br')) {
          el.innerHTML = translations[lang][key];
        } else {
          el.textContent = translations[lang][key];
        }
      }
    });
  }

  // Dynamic Discovery Replace for small text nodes
  const dynamicSelectors = '.feature-label, .feature-value, .vibe-label, .footer-tagline, .footer-link, .footer-copy, .radio-label span, .progress-card-title, .progress-list li';
  document.querySelectorAll(dynamicSelectors).forEach(el => {
    // Save original key on first run
    if (!el.dataset.i18nKey) {
      let text = el.textContent.trim();
      
      // If the node contains badge, get text without badge
      if (el.querySelector('.badge')) {
        text = el.childNodes[0].textContent.trim();
      }

      // Special edge cases for some Korean texts matching exactly
      const dict = translations.ko;
      const key = Object.keys(dict).find(k => dict[k] === text);
      if (key) {
        el.dataset.i18nKey = key;
      }
    }
    
    const key = el.dataset.i18nKey;
    if (key && translations[lang][key]) {
      const badge = el.querySelector('.badge');
      const statusDot = el.querySelector('.status-dot');
      if (badge) {
        // Special handle for progress title with badge
        el.innerHTML = translations[lang][key] + ' <span class="badge badge--active">' + translations[lang]['status_active'] + '</span>';
      } else if (statusDot) {
        // Preserve status dot
        el.innerHTML = statusDot.outerHTML + ' ' + translations[lang][key];
      } else if (el.classList.contains('feature-value--badge')) {
        // Special handle for badge inside feature-value
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });

  // Handle special case: form labels
  const formLabel = document.querySelector('.switch-interest legend.form-label');
  if (formLabel) formLabel.textContent = translations[lang]['label_interest'];
  const emailLabel = document.querySelector('.form-label[for="email-input"]');
  if (emailLabel) emailLabel.innerHTML = translations[lang]['label_email'] + ' <span class="required" aria-hidden="true">*</span>';
}

document.addEventListener('DOMContentLoaded', () => {
  detectLocationAndSetLanguage();
  
  // Custom dropdown events
  document.querySelectorAll('.custom-dropdown').forEach(dropdown => {
    const toggle = dropdown.querySelector('.dropdown-toggle');
    const menu = dropdown.querySelector('.dropdown-menu');
    
    toggle.addEventListener('click', (e) => {
      e.stopPropagation();
      
      // Close other dropdowns
      document.querySelectorAll('.custom-dropdown').forEach(d => {
        if (d !== dropdown) d.classList.remove('open');
      });
      
      dropdown.classList.toggle('open');
      toggle.setAttribute('aria-expanded', dropdown.classList.contains('open'));
    });
    
    menu.querySelectorAll('li').forEach(item => {
      item.addEventListener('click', () => {
        setLanguage(item.dataset.value);
        dropdown.classList.remove('open');
        toggle.setAttribute('aria-expanded', 'false');
      });
    });
  });
  
  // Close dropdown when clicking outside
  document.addEventListener('click', () => {
    document.querySelectorAll('.custom-dropdown').forEach(dropdown => {
      dropdown.classList.remove('open');
      dropdown.querySelector('.dropdown-toggle').setAttribute('aria-expanded', 'false');
    });
  });
});
